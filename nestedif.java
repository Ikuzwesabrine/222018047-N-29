//This program show nestedif 
public class nestedif { //class name
    public static void main(String[] args){
        int marks = 4;
         if (marks>15){ //condition
            System.out.println("a");
         } 
         else if (marks>10){
            System.out.println("b");
         } 
         else if (marks>5){
            System.out.println("c");
         } 
         else{
            System.out.println("F");
         }
    }
}
