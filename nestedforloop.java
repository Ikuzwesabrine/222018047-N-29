//This program show nestedforloop
public class nestedforloop { // class name
     public static void main(String[] args) {
        for(int i = 1; i <= 5; i++){
            for(int j=1; j<=i; j++){
                // This is output
                System.out.print (i);
            } 
            System.out.println();         
        }
     }
}
